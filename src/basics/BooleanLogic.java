package basics;

public class BooleanLogic {

    public static void main(String[] args) {
        boolean x = true;
        boolean y = false;

        System.out.println(x);
        System.out.println(!x);

        System.out.println(y);
        System.out.println(!y);

        System.out.println(x && y);
        System.out.println(x || y);
    }

}